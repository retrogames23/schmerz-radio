export interface FsFile {
  type: "file";
  name: string;
  kind?: "text" | "log" | "image" | "news" | "mail" | "audio" | "config";
  size?: number;
  date?: string;
  /** Optional StoryFlag required to reveal this file. */
  requires?: string;
  content: string[];
}

export interface FsDir {
  type: "dir";
  name: string;
  desc?: string;
  children: FsNode[];
}

