/** Back-compat: nur Pfade. Neuer Code sollte DSA_SCENE_CATALOG nutzen. */
export const DSA_SCENE_IMAGES: Record<string, string> = Object.fromEntries(
  Object.entries(DSA_SCENE_CATALOG).map(([k, v]) => [k, v.src]),
);

export const DSA_SCENE_TAGS = Object.keys(DSA_SCENE_CATALOG) as ReadonlyArray<string>;

/**
 * Formatierter Katalog für den LLM-Prompt: `tag — Beschreibung` pro Zeile.
 * Wird einmal beim Build der Prompts gerendert.
 */
export function formatSceneCatalogForPrompt(): string {
  return Object.entries(DSA_SCENE_CATALOG)
    .map(([tag, entry]) => `      ${tag} — ${entry.use}`)
    .join("\n");
}

/**
 * Liefert die zum Tag passende Illustration oder `null`, wenn kein
 * passendes Bild im Pool existiert. Der Aufrufer rendert dann kein Bild.
 */
export function resolveSceneImage(tag: string | null | undefined): string | null {
  if (!tag) return null;
  return DSA_SCENE_IMAGES[tag] ?? null;
}